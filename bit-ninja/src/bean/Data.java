package bean;

public class Data {
	private int userLogged;
	private int hardcore;
	private int softcore;
	
	public Data(){}

	public int getUserLogged() {
		return userLogged;
	}

	public void setUserLogged(int userLogged) {
		this.userLogged = userLogged;
	}

	public int getHardcore() {
		return hardcore;
	}

	public void setHardcore(int hardcore) {
		this.hardcore = hardcore;
	}

	public int getSoftcore() {
		return softcore;
	}

	public void setSoftcore(int softcore) {
		this.softcore = softcore;
	}
	
	
}
